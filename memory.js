/* =============================================
   Render Chapters
   ============================================= */

function renderChapters() {
  let pageNum = 1;
  allImages = [];

  BOOK.chapters.forEach((chapter, ci) => {
    const section = document.createElement('section');
    section.className = 'page chapter-page';
    section.id = chapter.id;
    section.dataset.chapter = `${chapter.number}: ${chapter.title}`;

    const textHtml = chapter.text.map((p) => `<p>${p}</p>`).join('');

    const count = chapter.images?.length || 0;
    const galleryClass = `gallery-${count}`;

    const photosHtml = (chapter.images || [])
      .map((img) => {
        const globalIdx = allImages.length;
        allImages.push(img);
        return `
          <figure class="photo-frame" data-index="${globalIdx}">
            <div class="photo-wrap">
              <img src="${img.src}" alt="${img.caption}" loading="lazy">
              <div class="photo-overlay">
                <span class="photo-zoom">&#128269;</span>
              </div>
            </div>
            <figcaption>${img.caption}</figcaption>
          </figure>`;
      })
      .join('');

    const videosHtml = (chapter.videos || [])
      .map(
        (vid) => `
          <div class="chapter-video-card">
            <video controls preload="metadata" playsinline>
              <source src="${vid.src}" type="video/mp4">
            </video>
            <p class="chapter-video-caption">${vid.caption}</p>
          </div>`
      )
      .join('');

    const slideHtml = (chapter.images || [])
      .map(
        (img) => `
          <div class="chapter-slide-card">
            <img src="${img.src}" alt="${img.caption}" loading="lazy">
            <p class="chapter-slide-caption">${img.caption}</p>
          </div>`
      )
      .join('');

    const mediaHtml = chapter.videos?.length
      ? `<div class="chapter-video-gallery">${videosHtml}</div>`
      : chapter.galleryType === 'slide'
        ? `<div class="chapter-slide-gallery">${slideHtml}</div>`
        : `<div class="photo-gallery ${galleryClass}">${photosHtml}</div>`;

    section.innerHTML = `
      <div class="page-corner page-corner-tr"></div>
      <div class="page-corner page-corner-bl"></div>
      <div class="page-inner">
        <span class="chapter-number">${chapter.number}</span>
        <h2 class="chapter-title">${chapter.title}</h2>
        <div class="title-ornament"><span>❦</span></div>
        <div class="chapter-body">${textHtml}</div>
        ${mediaHtml}
        <div class="page-footer">
          <span class="page-num">${pageNum++}</span>
        </div>
      </div>`;

    chaptersContainer.appendChild(section);
  });

  renderTOC();
  renderVideos();
  renderTimeline();
}

function renderTimeline() {
  const container = $('#timeline-cards');
  if (!container) return;

  container.innerHTML = BOOK.dates
    .map(
      (item) => `
      <div class="timeline-item${item.highlight ? ' timeline-highlight' : ''}">
        <div class="timeline-dot"></div>
        <div class="timeline-card">
          <span class="timeline-icon">${item.icon}</span>
          <div class="timeline-content">
            <span class="timeline-label">${item.label}</span>
            <span class="timeline-date">${item.date}</span>
            <p class="timeline-desc">${item.desc}</p>
          </div>
        </div>
      </div>`
    )
    .join('');
}

function renderTOC() {
  tocList.innerHTML = '';

  const items = [
    { id: 'prolog', label: 'Prolog — Sebelum Bab Pertama' },
    { id: 'timeline', label: 'Interlude — Tanggal-Tanggal Penting' },
    ...BOOK.chapters.map((c) => ({ id: c.id, label: `${c.number} — ${c.title}` })),
    ...(BOOK.videos.length ? [{ id: 'video-section', label: 'Film — Gerak & Suara' }] : []),
    { id: 'epilog', label: 'Epilog — Bab Selanjutnya' },
  ];

  items.forEach((item) => {
    const li = document.createElement('li');
    const a = document.createElement('a');
    a.href = `#${item.id}`;
    a.textContent = item.label;
    a.addEventListener('click', (e) => {
      e.preventDefault();
      closeTOC();
      document.getElementById(item.id)?.scrollIntoView({ behavior: 'smooth' });
    });
    li.appendChild(a);
    tocList.appendChild(li);
  });
}

function renderVideos() {
  const videoSection = $('#video-section');
  const videoGallery = $('#video-gallery');

  if (!BOOK.videos.length) {
    videoSection.classList.add('hidden');
    return;
  }

  videoSection.classList.remove('hidden');
  videoGallery.innerHTML = BOOK.videos
    .map(
      (vid) => `
      <div class="video-frame">
        <video controls preload="metadata" playsinline>
          <source src="${vid.src}" type="video/mp4">
        </video>
        <p class="video-caption">${vid.caption}</p>
      </div>`
    )
    .join('');
}

/* =============================================
   Loader & Open Book
   ============================================= */

function hideLoader() {
  loader.classList.add('fade-out');
  setTimeout(() => loader.remove(), 800);
}

function openBook() {
  if (bookOpened) return;
  bookOpened = true;

  $('#cover').scrollIntoView({ behavior: 'smooth' });
  setTimeout(() => {
    $('#prolog').scrollIntoView({ behavior: 'smooth' });
  }, 600);

  bookNav.classList.remove('hidden');
  readingProgress.classList.remove('hidden');
  musicPlayer.classList.remove('hidden');

  bgMusic.volume = 0.4;
  bgMusic.play().catch(() => {
    document.addEventListener('click', () => bgMusic.play(), { once: true });
  });
}
function autoStartBook() {
  if (!$('#cover')) {
    bookOpened = true;
    bookNav.classList.remove('hidden');
    readingProgress.classList.remove('hidden');
    musicPlayer.classList.remove('hidden');
  }
}
/* =============================================
   Music Player
   ============================================= */

function initMusic() {
  const toggle = $('#music-toggle');
  const mute = $('#music-mute');
  const progress = $('#music-progress');
  const progressWrap = $('#music-progress-wrap');

  toggle.addEventListener('click', () => {
    if (bgMusic.paused) {
      bgMusic.play();
      toggle.querySelector('.icon-play').classList.add('hidden');
      toggle.querySelector('.icon-pause').classList.remove('hidden');
    } else {
      bgMusic.pause();
      toggle.querySelector('.icon-play').classList.remove('hidden');
      toggle.querySelector('.icon-pause').classList.add('hidden');
    }
  });

  mute.addEventListener('click', () => {
    bgMusic.muted = !bgMusic.muted;
    mute.querySelector('.icon-volume').classList.toggle('hidden', bgMusic.muted);
    mute.querySelector('.icon-muted').classList.toggle('hidden', !bgMusic.muted);
  });

  if (progressWrap) {
    const seek = (event) => {
      const rect = progressWrap.getBoundingClientRect();
      const x = event.clientX - rect.left;
      const pct = Math.min(Math.max(x / rect.width, 0), 1);
      if (!isNaN(bgMusic.duration) && bgMusic.duration > 0) {
        bgMusic.currentTime = pct * bgMusic.duration;
      }
    };

    progressWrap.addEventListener('click', seek);
  }

  bgMusic.addEventListener('timeupdate', () => {
    const pct = (bgMusic.currentTime / bgMusic.duration) * 100 || 0;
    progress.style.width = `${pct}%`;
    if (progressWrap) {
      progressWrap.setAttribute('aria-valuenow', `${Math.round(pct)}`);
    }
  });

  bgMusic.addEventListener('play', () => {
    toggle.querySelector('.icon-play').classList.add('hidden');
    toggle.querySelector('.icon-pause').classList.remove('hidden');
  });
}

/* =============================================
   Lightbox
   ============================================= */

function openLightbox(index) {
  lightboxIndex = index;
  const img = allImages[index];
  lightboxImg.src = img.src;
  lightboxCaption.textContent = img.caption;
  lightbox.classList.remove('hidden');
  document.body.style.overflow = 'hidden';
}

function closeLightbox() {
  lightbox.classList.add('hidden');
  document.body.style.overflow = '';
}

function navigateLightbox(dir) {
  lightboxIndex = (lightboxIndex + dir + allImages.length) % allImages.length;
  const img = allImages[lightboxIndex];
  lightboxImg.src = img.src;
  lightboxCaption.textContent = img.caption;
}

function initLightbox() {
  chaptersContainer.addEventListener('click', (e) => {
    const frame = e.target.closest('.photo-frame');
    if (!frame) return;
    openLightbox(parseInt(frame.dataset.index, 10));
  });

  $('#lightbox-close').addEventListener('click', closeLightbox);
  $('#lightbox-prev').addEventListener('click', () => navigateLightbox(-1));
  $('#lightbox-next').addEventListener('click', () => navigateLightbox(1));

  lightbox.addEventListener('click', (e) => {
    if (e.target === lightbox) closeLightbox();
  });

  document.addEventListener('keydown', (e) => {
    if (lightbox.classList.contains('hidden')) return;
    if (e.key === 'Escape') closeLightbox();
    if (e.key === 'ArrowLeft') navigateLightbox(-1);
    if (e.key === 'ArrowRight') navigateLightbox(1);
  });
}

/* =============================================
   Navigation & Scroll
   ============================================= */

function initNavigation() {
  const openBookBtn = $('#open-book');
  if (openBookBtn) {
    openBookBtn.addEventListener('click', openBook);
  }
  $('#nav-toc').addEventListener('click', () => tocOverlay.classList.remove('hidden'));
  $('#toc-close').addEventListener('click', closeTOC);
  tocOverlay.addEventListener('click', (e) => {
    if (e.target === tocOverlay) closeTOC();
  });
  $('#nav-top').addEventListener('click', () => {
    window.scrollTo({ top: 0, behavior: 'smooth' });
  });
}

function closeBook() {
  if (!closeBookBtn || closeBookBtn.classList.contains('is-closing')) return;

  closeBookBtn.classList.add('is-closing');
  document.body.classList.add('book-closing');
  bookCloseOverlay.classList.add('show');

  setTimeout(() => {
    window.location.href = 'index.html';
  }, 1400);
}

function initCloseBook() {
  if (closeBookBtn) {
    closeBookBtn.addEventListener('click', closeBook);
  }
}

function closeTOC() {
  tocOverlay.classList.add('hidden');
}

function initScrollEffects() {
  const readingBar = $('#reading-bar');
  const navChapter = $('#nav-chapter');

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
          if (entry.target.dataset.chapter) {
            navChapter.textContent = entry.target.dataset.chapter;
          }
        }
      });
    },
    { threshold: 0.15 }
  );

  $$('.page').forEach((page) => observer.observe(page));

  window.addEventListener('scroll', () => {
    const scrollTop = window.scrollY;
    const docHeight = document.documentElement.scrollHeight - window.innerHeight;
    const pct = docHeight > 0 ? (scrollTop / docHeight) * 100 : 0;
    readingBar.style.width = `${pct}%`;
  }, { passive: true });
}

/* =============================================
   Background Decorations
   ============================================= */

function initDecorations() {
  const heartsContainer = $('.floating-hearts');
  const sparklesContainer = $('.floating-sparkles');
  if (!heartsContainer || !sparklesContainer) return;

  const heartChars = ['♥', '♡', '❤', '✦'];
  const sparkleChars = ['✧', '·', '✦', '°'];

  for (let i = 0; i < 12; i++) {
    const el = document.createElement('span');
    el.className = 'float-heart';
    el.textContent = heartChars[i % heartChars.length];
    el.style.left = `${Math.random() * 100}%`;
    el.style.animationDuration = `${12 + Math.random() * 18}s`;
    el.style.animationDelay = `${Math.random() * 10}s`;
    el.style.fontSize = `${0.6 + Math.random() * 0.8}rem`;
    el.style.opacity = `${0.08 + Math.random() * 0.15}`;
    heartsContainer.appendChild(el);
  }

  for (let i = 0; i < 20; i++) {
    const el = document.createElement('span');
    el.className = 'float-sparkle';
    el.textContent = sparkleChars[i % sparkleChars.length];
    el.style.left = `${Math.random() * 100}%`;
    el.style.animationDuration = `${8 + Math.random() * 14}s`;
    el.style.animationDelay = `${Math.random() * 8}s`;
    el.style.fontSize = `${0.4 + Math.random() * 0.6}rem`;
    el.style.opacity = `${0.1 + Math.random() * 0.2}`;
    sparklesContainer.appendChild(el);
  }
}

